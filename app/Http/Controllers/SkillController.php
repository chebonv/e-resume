    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $prof=Profession::firstOrCreate(
            ['profession'=>$request->career_option]
        );
        $skill=new Skill();
        $skill->user_id=Auth::user()->id;
        $skill->skill=$request->expertise;
        $skill->career_option=$prof->id;
        if ($skill->save())
        {
            return redirect()->back()->with("success","Skilled Saved Successfully!");
        }
        return redirect()->back()->with("error","Skilled Not Saved Successfully!");
    }

